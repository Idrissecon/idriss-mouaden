grant usage on schema public to anon, authenticated;
grant select on public.content_items to anon, authenticated;
grant insert, update, delete on public.content_items to authenticated;
grant usage, select on sequence public.content_items_id_seq to authenticated;
grant execute on function public.is_site_owner() to anon, authenticated;

create policy "Published content is publicly readable"
on public.content_items for select to anon, authenticated
using (status = 'published' or public.is_site_owner());

create policy "Owner can create content"
on public.content_items for insert to authenticated
with check (public.is_site_owner());

create policy "Owner can update content"
on public.content_items for update to authenticated
using (public.is_site_owner()) with check (public.is_site_owner());

create policy "Owner can delete content"
on public.content_items for delete to authenticated
using (public.is_site_owner());

