create table public.content_items (
  id bigint generated by default as identity primary key,
  title text not null,
  slug text not null unique,
  category text not null check (category in ('research', 'writing')),
  status text not null default 'draft' check (status in ('draft', 'published')),
  summary text not null default '',
  body text not null default '',
  venue text,
  year integer check (year is null or year between 1900 and 2200),
  publication_date date,
  featured boolean not null default false,
  external_url text,
  document_key text,
  document_name text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index content_items_public_listing_idx
  on public.content_items (status, category, featured desc, publication_date desc, year desc);

alter table public.content_items enable row level security;

create or replace function public.is_site_owner()
returns boolean
language sql
stable
set search_path = ''
as $$
  select lower(coalesce(auth.jwt() ->> 'email', '')) = 'mouadenidriss574@gmail.com'
